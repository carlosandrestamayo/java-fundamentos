public class OperadoresUnarios {

    public static void main(String[] args) {

        int x = 5;

        System.out.println("x inicial: " + x);
        System.out.println("Postincremento: " + x++);
        System.out.println("Después del postincremento: " + x);

        System.out.println("Preincremento: " + ++x);
        System.out.println("Después del preincremento: " + x);

        System.out.println("Negativo: " + (-x));
    }
}
