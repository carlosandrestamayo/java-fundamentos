public class OperadoresAsignacion {

    public static void main(String[] args) {

        int x = 10;

        x += 5;   // x = x + 5
        x -= 2;   // x = x - 2
        x *= 3;   // x = x * 3
        x /= 4;   // x = x / 4
        x %= 3;   // x = x % 3

        System.out.println("Valor final de x: " + x);
    }
}
