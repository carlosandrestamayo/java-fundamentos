public class IfElseIf {

    public static void main(String[] args) {

        int calificacion = 85;

        if (calificacion >= 90) {
            System.out.println("Excelente");
        } else if (calificacion >= 70) {
            System.out.println("Bueno");
        } else {
            System.out.println("Insuficiente");
        }

    }
}
